# we can calculate two numbers with the help of arthmatic operators
# +, -, *, /, %, **, //

num1 = 15
num2 = 8
print("Number 1: ", num1)
print("Number 2: ", num2)

# addition
result = num1 + num2
print("Addition: ", result)

# subtraction
result = num1 - num2
print("Subtraction: ", result)

# multiplication
result = num1 * num2
print("Multiplication: ", result)

# division
result = num1 / num2
print("Division: ", result)

# modulus
result = num1 % num2
print("Modulus: ", result)

# exponent
result = num1 ** num2
print("Exponent: ", result)

# floor division
result = num1 // num2
print("Floor Division: ", result)