print("Hello, World!")


# This is Escape Sequence Character of new line
print("I am a Python program.\nand this learner is a Python learner.")

# This is Escape Sequence Character of qoutation mark
print("I am a \"Python program\".")

# we can use multiple values in print statement
print("Hello", "World", "Python", "Learner")

# we can use multiple values in print statement with separator
print("Hello", "World", "Python", "Learner", sep=", ")

# we can use multiple values in print statement with separator and end
print("Hello", "World", "Python", "Learner", sep=", ", end="!\n")
